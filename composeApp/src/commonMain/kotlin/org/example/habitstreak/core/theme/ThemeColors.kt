/**
 * Get color scheme for the specified theme
 */
fun getColorSchemeForTheme(
    appTheme: AppTheme,
    isSystemInDarkMode: Boolean = false
): ColorScheme {
    return when (appTheme) {
        AppTheme.SYSTEM -> if (isSystemInDarkMode) DarkColorScheme else LightColorScheme
        AppTheme.LIGHT -> LightColorScheme
        AppTheme.DARK -> DarkColorScheme
        AppTheme.PURPLE -> PurpleColorScheme
        AppTheme.GREEN -> GreenColorScheme
        AppTheme.BLUE -> BlueColorScheme
    }
}